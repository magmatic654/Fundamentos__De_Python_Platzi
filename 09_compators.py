# < menor que
print("Menor que")
print(f"2 < 8 : {2 < 8}")
print(f"10 < 5: {10 < 5}")
print(f"3 < 3 : {3 < 3} \n")

# >= mayor o igual que
print("Mayor o igual que")
print(f"5 >= 2: {5 >= 2}")
print(f"6 >= 9: {6 >= 9}")
print(f"4 >= 4: {4 >= 4}\n")

# <= menor o igual que
print("Menor o igual que")
print(f"5 <= 2: {5 <= 2}")
print(f"6 <= 5: {6 <= 5}")
print(f"6 <= 6: {6 <= 6}\n")

# == igual que
print("Igual que")
print(f"1 == 4: {1 == 4}")
print(f"5 == 7: {5 == 7}")
print(f"2 == 2: {2 == 2}\n")

# != diferente que
print("Diferente que")
print(f"6 != 10: {6 != 10}")
